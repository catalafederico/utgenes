package main.java.ar.edu.utn.frba.ia.ag.acertijoelfos;

public class Elfo {
	
	public String nombre;
	public String departamento;
	public String colorDePelo;
	public String colorDeZuecos;
	public String trabajo;
	public String mascota;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDepartamento() {
		return departamento;
	}
	public void setDepartamento(String departamento) {
		this.departamento = departamento;
	}
	public String getColorDePelo() {
		return colorDePelo;
	}
	public void setColorDePelo(String colorDePelo) {
		this.colorDePelo = colorDePelo;
	}
	public String getColorDeZuecos() {
		return colorDeZuecos;
	}
	public void setColorDeZuecos(String colorDeZuecos) {
		this.colorDeZuecos = colorDeZuecos;
	}
	public String getTrabajo() {
		return trabajo;
	}
	public void setTrabajo(String trabajo) {
		this.trabajo = trabajo;
	}
	public String getMascota() {
		return mascota;
	}
	public void setMascota(String mascota) {
		this.mascota = mascota;
	}

}
